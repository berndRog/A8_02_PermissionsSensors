package de.rogallab.mobile.data.remote.network

// helper function to get the http status message from the http status code
fun httpStatusMessage(code:Int) : String {
      val httpMessage = "$code: ,"
      return when (code) {
            200 -> httpMessage + "OK"
            201 -> httpMessage + "Created"
            202 -> httpMessage + "Accepted"
            203 -> httpMessage + "Non-Authoritative Information"
            204 -> httpMessage + "No Content"
            205 -> httpMessage + "Reset Content"
            206 -> httpMessage + "Partial Content"
            300 -> httpMessage + "Multiple Choices"
            301 -> httpMessage + "Moved Permanently"
            302 -> httpMessage + "Found"
            303 -> httpMessage + "Other"
            304 -> httpMessage + "Modified"
            305 -> httpMessage + "Use Proxy"
            307 -> httpMessage + "Temporary Redirect"
            308 -> httpMessage + "Permanent Redirect"
            400 -> httpMessage + "Bad Request"
            401 -> httpMessage + "Unauthorized"
            403 -> httpMessage + "Forbidden"
            404 -> httpMessage + "Not Found"
            405 -> httpMessage + "Method Not Allowed"
            406 -> httpMessage + "Not Acceptable"
            407 -> httpMessage + "Proxy Authentication Required"
            408 -> httpMessage + "Request Timeout"
            409 -> httpMessage + "Conflict"
            410 -> httpMessage + "Gone"
            411 -> httpMessage + "Length Required"
            412 -> httpMessage + "Precondition Failed"
            413 -> httpMessage + "Payload Too Large"
            414 -> httpMessage + "URI Too Long"
            415 -> httpMessage + "Unsupported Media Type"
            416 -> httpMessage + "Range Not Satisfiable"
            417 -> httpMessage + "Expectation Failed"
            418 -> httpMessage + "I'm a teapot"
            421 -> httpMessage + "Misdirected Request"
            422 -> httpMessage + "Unprocessable Entity"
            423 -> httpMessage + "Locked"
            424 -> httpMessage + "Failed Dependency"
            425 -> httpMessage + "Too Early"
            426 -> httpMessage + "Upgrade Required"
            428 -> httpMessage + "Precondition Required"
            429 -> httpMessage + "Too Many Requests"
            431 -> httpMessage + "Request Header Fields Too Large"
            451 -> httpMessage + "Unavailable For Legal Reasons"
            500 -> httpMessage + "Internal Server Error"
            501 -> httpMessage + "Not Implemented"
            502 -> httpMessage + "Bad Gateway"
            503 -> httpMessage + "Service Unavailable"
            504 -> httpMessage + "Gateway Timeout"
            505 -> httpMessage + "HTTP Version Not Supported"
            506 -> httpMessage + "Variant Also Negotiates"
            507 -> httpMessage + "Insufficient Storage"
            508 -> httpMessage + "Loop Detected"
            510 -> httpMessage + "Not Extended"
            511 -> httpMessage + "Network Authentication Required"
            else -> httpMessage + "unknown $code"
      }
}
